// Package types 是 Polaris 全域的 L0 净化区。
//
// # 定位
//
// 本包是全系统最底层的纯数据契约仓库，所有业务模块均可安全导入。
// 本包的设计遵循以下强制约束：
//
//   - [限制] 仅允许包含基础的结构体（models）、枚举（enums）和常量（consts）。
//   - [允许] 允许枚举或结构体包含高内聚的纯内存计算方法/函数（例如 TaintLevel 的传播逻辑、TrustTier 的纯映射逻辑），但绝对禁止任何外部系统交互（I/O）。
//   - [禁止] 导入任何 internal/* 业务模块
//   - [禁止] 定义可变全局变量（并发安全 + 测试隔离）
//   - [禁止] 定义任何 interface（接口必须在消费方模块内部定义，见 docs/arch/Module-Dependency-Axioms.md §2.1）
//
// # 内容分布
//
//   - enums.go  — 全域枚举类型（AgentState、TaintLevel、RiskLevel 等）
//   - models.go — 跨模块共享纯数据结构（Usage、ScoredFragment、DB Row 等）
//   - consts.go — 全域常量（物理边界、默认阈值）
